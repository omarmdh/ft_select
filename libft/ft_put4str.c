#include "libft.h"

int		ft_put4str(char *s1, char *s2, char *s3, char *s4)
{
	ft_putstr_fd(s1, 0);
	ft_putstr_fd(s2, 0);
	ft_putstr_fd(s3, 0);
	ft_putstr_fd(s4, 0);
	return (1);
}
